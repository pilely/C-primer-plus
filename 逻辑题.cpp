int MinTimes(int a[], int n)
{
	int i, abcd[4] = { 0 };
	for (i = 0; i < n; i++) abcd[a[1 + i]]++;

	int min = abcd[0], j = 0;
	for (i = 0; i < 4; i++)
	{
		if (min > abcd[i]) { min = abcd[i]; j = i; }
	}
	return j;
}

